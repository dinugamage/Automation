package com.java.general;

public class StringConcatenation {

    public static void main(String[] args) {
    
        String param1 = "Hello";
        String param2 = "All";
        String param3 = "Guys";
   
        String sentence = param1 + " " + param2 + " " + param3;
        
        System.out.println(sentence);
        
    }
}
